// variables

// variable declaration
// mutable: can be changed
let num = 100
console.log(num)
console.log("num = " + num)

// change the value of num
num = 200
console.log("num = ", num)
console.log(`num = ${num}`)


// constants
// immutable: can not change

console.log(`pi = ${pi}`)

