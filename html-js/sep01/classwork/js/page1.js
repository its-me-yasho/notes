function function1() {
    // variable
    let num = 100
    console.log(`num = ${num}`)

    // constant
    const address = 'pune'
    console.log(`address = ${address}`)
}


// function1()


function function2() {
    // in js, data type is inferred

    // number
    let myVar = 100
    console.log(`myVar = ${myVar}, type = ${typeof myVar}`)

    // string
    myVar = 'test'
    console.log(`myVar = ${myVar}, type = ${typeof myVar}`)

    // boolean
    myVar = true
    console.log(`myVar = ${myVar}, type = ${typeof myVar}`)

    // undefined
    myVar = undefined
    console.log(`myVar = ${myVar}, type = ${typeof myVar}`)

    // object
    myVar = {model: 'i20', company: 'hyundai'}
    console.log(`myVar = ${myVar}, type = ${typeof myVar}`)

    // number
    myVar = 26.70
    console.log(`myVar = ${myVar}, type = ${typeof myVar}`)
}

// function2()

